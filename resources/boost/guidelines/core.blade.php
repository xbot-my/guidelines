# Project Coding Guidelines

- This codebase follows XBot's coding guidelines.
- Always activate the `xbot-laravel-php` skill when writing, editing, reviewing, or formatting Laravel or PHP code.
- Always activate the `xbot-version-control` skill when creating commits, branches, or managing Git operations.
- Always activate the `xbot-security` skill when configuring security, reviewing authentication, or setting up servers and databases.
